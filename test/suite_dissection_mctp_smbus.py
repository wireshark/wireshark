class TestMctpSmbusLengthMismatch:
    """Length-mismatch expert info tests (frame 9)."""

    FILE = 'mctp-smbus.pcapng'

    def test_extra_trailing_bytes_detected_as_mctp_smbus(self, cmd_tshark, capture_file, test_env):
        """Frame 9: extra trailing bytes still pass the heuristic and are decoded."""
        stdout = subprocess.check_output((cmd_tshark,
            '-r', capture_file(self.FILE),
            '-Y', 'frame.number == 9 && mctp.smbus',
            '-Tfields', '-e', 'frame.number',
        ), encoding='utf-8', env=test_env)
        assert stdout.strip() == '9'

    def test_extra_trailing_bytes_raises_length_mismatch(self, cmd_tshark, capture_file, test_env):
        """Frame 9: ei_mctp_smbus_length_mismatch expert info is raised."""
        stdout = subprocess.check_output((cmd_tshark,
            '-r', capture_file(self.FILE),
            '-Y', 'frame.number == 9 && mctp.smbus.length_mismatch',
            '-Tfields', '-e', 'frame.number',
        ), encoding='utf-8', env=test_env)
        assert stdout.strip() == '9'

    def test_extra_trailing_bytes_inner_mctp_decoded(self, cmd_tshark, capture_file, test_env):
        """Frame 9: despite the warning, the inner MCTP frame is decoded (mctp.dst present)."""
        stdout = subprocess.check_output((cmd_tshark,
            '-r', capture_file(self.FILE),
            '-Y', 'frame.number == 9 && mctp.dst == 0x20',
            '-Tfields', '-e', 'frame.number',
        ), encoding='utf-8', env=test_env)
        assert stdout.strip() == '9'

    def test_pec_is_last_byte_when_extra_trailing(self, cmd_tshark, capture_file, test_env):
        """Frame 9: PEC is decoded from the last byte (0xad), not from byte_count+3.

        SMBus appends PEC at the end of the transmission.  When byte_count is
        wrong (too small), the extra bytes fall between the byte_count-indicated
        MCTP payload end and PEC at len-1; the dissector must not consume them
        as PEC.
        """
        stdout = subprocess.check_output((cmd_tshark,
            '-r', capture_file(self.FILE),
            '-Y', 'frame.number == 9 && mctp.smbus.pec == 0xab',
            '-Tfields', '-e', 'frame.number',
        ), encoding='utf-8', env=test_env)
        assert stdout.strip() == '9'


class TestMctpSmbusTruncated:
    """Snaplen-truncated frame regression tests (frame 10).

    Verifies that mctp_smbus_frame_is_valid() returns false safely when
    tvb_captured_length < MCTP_SMBUS_MIN_LENGTH <= tvb_reported_length,
    rather than throwing a BoundsError out of dissector_try_heuristic().
    """

    FILE = 'mctp-smbus.pcapng'

    def test_truncated_not_detected_as_mctp_smbus(self, cmd_tshark, capture_file, test_env):
        """Frame 10: truncated I2C frame is NOT decoded as mctp.smbus."""
        stdout = subprocess.check_output((cmd_tshark,
            '-r', capture_file(self.FILE),
            '-Y', 'frame.number == 10 && mctp.smbus',
            '-Tfields', '-e', 'frame.number',
        ), encoding='utf-8', env=test_env)
        assert stdout.strip() == ''

    def test_truncated_decoded_as_i2c(self, cmd_tshark, capture_file, test_env):
        """Frame 10: truncated I2C frame is still visible as i2c (not malformed)."""
        stdout = subprocess.check_output((cmd_tshark,
            '-r', capture_file(self.FILE),
            '-Y', 'frame.number == 10 && i2c',
            '-Tfields', '-e', 'frame.number',
        ), encoding='utf-8', env=test_env)
        assert stdout.strip() == '10'
